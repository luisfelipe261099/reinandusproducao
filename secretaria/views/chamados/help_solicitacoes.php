<div class="bg-white shadow-md rounded-lg p-6">
    <h1 class="text-2xl font-bold mb-6">Ajuda - Solicitações de Documentos</h1>
    
    <div class="space-y-6">
        <div>
            <h2 class="text-xl font-semibold mb-2">O que são solicitações de documentos?</h2>
            <p class="text-gray-700">
                Solicitações de documentos são pedidos feitos pelos polos para geração de documentos acadêmicos, como declarações de matrícula e históricos escolares. Quando um polo solicita um documento, a secretaria acadêmica recebe a solicitação e pode processá-la para gerar o documento requisitado.
            </p>
        </div>
        
        <div>
            <h2 class="text-xl font-semibold mb-2">Status das solicitações</h2>
            <ul class="list-disc pl-6 space-y-2">
                <li>
                    <span class="font-medium">Solicitado:</span>
                    <span class="text-gray-700">Solicitação enviada pelo polo e aguardando processamento pela secretaria acadêmica.</span>
                </li>
                <li>
                    <span class="font-medium">Processando:</span>
                    <span class="text-gray-700">Solicitação em análise pela secretaria acadêmica.</span>
                </li>
                <li>
                    <span class="font-medium">Pronto:</span>
                    <span class="text-gray-700">Documento gerado e disponível para download.</span>
                </li>
                <li>
                    <span class="font-medium">Entregue:</span>
                    <span class="text-gray-700">Documento foi entregue ao solicitante/aluno.</span>
                </li>
                <li>
                    <span class="font-medium">Cancelado:</span>
                    <span class="text-gray-700">Solicitação foi cancelada pela secretaria acadêmica ou pelo polo.</span>
                </li>
            </ul>
        </div>
        
        <div>
            <h2 class="text-xl font-semibold mb-2">Como responder a uma solicitação?</h2>
            <ol class="list-decimal pl-6 space-y-2">
                <li>
                    <span class="text-gray-700">Acesse a lista de solicitações clicando em "Solicitações de Documentos" no menu.</span>
                </li>
                <li>
                    <span class="text-gray-700">Clique no ícone de visualização (<i class="fas fa-eye"></i>) para ver os detalhes da solicitação.</span>
                </li>
                <li>
                    <span class="text-gray-700">Clique no botão "Responder" para processar a solicitação.</span>
                </li>
                <li>
                    <span class="text-gray-700">Selecione o novo status da solicitação e adicione observações, se necessário.</span>
                </li>
                <li>
                    <span class="text-gray-700">Clique em "Responder Solicitação" para salvar.</span>
                </li>
                <li>
                    <span class="text-gray-700">Se o status for alterado para "Pronto", o sistema irá gerar automaticamente o documento.</span>
                </li>
            </ol>
        </div>
        
        <div>
            <h2 class="text-xl font-semibold mb-2">Dúvidas frequentes</h2>
            
            <div class="space-y-3 mt-3">
                <div>
                    <h3 class="font-medium">Como filtrar solicitações por polo?</h3>
                    <p class="text-gray-700">Use o filtro "Polo" disponível na parte superior da página de solicitações.</p>
                </div>
                
                <div>
                    <h3 class="font-medium">Posso editar um documento já gerado?</h3>
                    <p class="text-gray-700">Não, uma vez que o documento é gerado, não é possível editá-lo. Caso seja necessário fazer alterações, cancele a solicitação atual e solicite um novo documento.</p>
                </div>
                
                <div>
                    <h3 class="font-medium">Os alunos são notificados sobre o status da solicitação?</h3>
                    <p class="text-gray-700">Sim, quando o status da solicitação é alterado para "Processando", "Pronto" ou "Cancelado", o sistema envia automaticamente um e-mail para o aluno informando sobre a atualização.</p>
                </div>
            </div>
        </div>
    </div>
</div>